#define CTLR_ID 0x00000000  // Edit this to make it unique
#define SEC_KEY 0x12345678  // Should be unique too

#define OP_QURYID 0
#define OP_GENTKN 1
#define OP_ATHOPN 2
#define OP_FRCOPN 3

#define REP_OK 0
#define REP_NO 1

typedef struct {
  uint32_t op;
  uint32_t data;
} usb_cmd_t;

static uint32_t latest_rand = 0;

uint16_t usb_rx_queue = 0;

static uint16_t door_op_queue = 0;

static int verify_sign(uint32_t token, uint32_t sign) {
  uint32_t buf[] = {SEC_KEY, token};
  uint32_t csign = HAL_CRC_Calculate(&hcrc, buf, 2);
  return sign == csign ? 0 : 1;
}

static void usb_echo_task(void *arg) {
  usb_cmd_t cmd;
  for (;;) {
    if (queue_pull(usb_rx_queue, &cmd, NO_TIMEOUT) < 0) {
      continue;
    }
    switch (cmd.op) {
    case OP_QURYID:
      cmd.data = CTLR_ID;
      USBD_CUSTOM_HID_SendReport(&hUsbDeviceFS, &cmd, sizeof(usb_cmd_t));
      break;
    case OP_GENTKN:
      cmd.data = latest_rand = rand();
      USBD_CUSTOM_HID_SendReport(&hUsbDeviceFS, &cmd, sizeof(usb_cmd_t));
      break;
    case OP_ATHOPN:
      if (latest_rand == 0 || verify_sign(latest_rand, cmd.data) != 0) {
        latest_rand = 0;
        cmd.data = REP_NO;
        USBD_CUSTOM_HID_SendReport(&hUsbDeviceFS, &cmd, sizeof(usb_cmd_t));
        break;
      }
      latest_rand = 0;
    case OP_FRCOPN:
      if (CTLR_ID == 0) {
        cmd.data = REP_OK;
        queue_push(door_op_queue, "", 0);
      } else {
        cmd.data = REP_NO;
      }
      USBD_CUSTOM_HID_SendReport(&hUsbDeviceFS, &cmd, sizeof(usb_cmd_t));
      break;
    }
  }
}

static void door_task(void *arg) {
  for (;;) {
    if (queue_pull(door_op_queue, "", NO_TIMEOUT) < 0) {
      continue;
    }
    // HAL_GPIO_WritePin(GPIOB, GPIO_PIN_1, GPIO_PIN_SET);
    HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_SET);
    task_sleep(1000);
    // HAL_GPIO_WritePin(GPIOB, GPIO_PIN_1, GPIO_PIN_RESET);
    HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_RESET);
  }
}

